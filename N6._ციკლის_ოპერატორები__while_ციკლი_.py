i=int(input("შეიყვანე რიცხვი:"))

y=1

while i<=10:
    # აქ შემოსულ რიცხვს ემატება  y და უკან ბრუნდება სანამ i მდე არ ავა
    i=i+y
    #ყოველი შემოსულისთვის დაბეჭდავს მიღებულ i_ს
    print(i)